  ULICA INT NOT NULL,
  MIESZKANIE INT NOT NULL,
  KOD_POCZTOWY INT NOT NULL,
  PRIMARY KEY (ID_ADRESS)
);

CREATE TABLE OSOBA
(
  ID_OSOBA INT NOT NULL,
  IMIE INT NOT NULL,
  NAZWISKO INT NOT NULL,
  PESEL INT NOT NULL,
  DATA_URODZENIA INT NOT NULL,
  TELEFON INT NOT NULL,
  EMAIL INT NOT NULL,
  PRIMARY KEY (ID_OSOBA)
);

CREATE TABLE DANE_LOGOWANIA
(
  ID_LOGOWANIA INT NOT NULL,
  LOGIN INT NOT NULL,
  HASLO INT NOT NULL,
  PRIMARY KEY (ID_LOGOWANIA)
